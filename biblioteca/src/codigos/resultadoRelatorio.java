    public static void Manipulacao2 (String registro) throws FileNotFoundException, IOException{
        BufferedReader fr = new BufferedReader(new FileReader("Emprestimos.txt"));
        String Registro1[] = new String[1000];
        String titulo2, autor2, genero2, anodolivro2, editora2;

        int verificador = 0;

        try{
            String linhalida;
            int q= 0;

            while(fr.ready() && verificador==0){
                linhalida = fr.readLine();
                String[] campos = linhalida.split(";");
                Registro1[q] = campos[10];

                if(registro.equals(Registro1[q])){
                    verificador = 1;
                    titulo2 = campos[0];
                    anodolivro2 = campos[1];
                    editora2 = campos[2];
                    autor2 = campos[3];
                    genero2 = campos[4];
                    editar3(titulo2, autor2, genero2, anodolivro2, editora2);
                    Apagardevolucao4(titulo2, autor2, genero2, anodolivro2, editora2);
                 }
            }
            fr.close(); 
        }
        catch(IOException e){}
    }
    public static void editar3(String titulo2, String autor2, String genero2, String anodolivro2, String editora2) throws FileNotFoundException, IOException{
        BufferedReader fr = new BufferedReader(new FileReader("Acervo.txt"));

        String titulo3[] = new String[200];
        String autor3[] = new String[200];
        String genero3[] = new String[200];
        String anodolivro3[] = new String[200]; 
        String editora3[] = new String[200];
        int status3[] = new int[200];
        try{
            String linhalida;
            int q= 0;

            while(fr.ready()){
            linhalida = fr.readLine();
            String[] campos = linhalida.split(";");
            titulo3[q] = campos[0];
            anodolivro3[q] = campos[1];
            editora3[q] = campos[2];
            autor3[q] = campos[3];
            genero3[q] = campos[4];
            status3[q] = Integer.parseInt(campos[5]);

            if(titulo2.equals(titulo3[q]) && autor2.equals(autor3[q]) && genero2.equals(genero3[q]) && anodolivro2.equals(anodolivro3[q]) && editora2.equals(editora3[q])){
                status3[q] = 1;
            }
            q++;
            }

            BufferedWriter fw = new BufferedWriter(new FileWriter("Acervo.txt"));          
            PrintWriter pw = new PrintWriter (fw);

            for(int i = 0;i<q;i++){
                pw.println(titulo3[i]+";"+anodolivro3[i]+";"+editora3[i]+";"+autor3[i]+";"+genero3[i]+";"+status3[i]);
            }
            fw.close();
            fr.close();
        } catch (IOException e){}
    }
