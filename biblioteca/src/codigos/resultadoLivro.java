    private void Arquivo2() throws IOException {
        BufferedReader fr = new BufferedReader(new FileReader("pesquisa_resultadoE.txt"));
        
        try {
            String linha;
            
            while(fr.ready()){
                linha = fr.readLine();
                String[] campos = linha.split(";");
                titulo2 = campos[0];
                anodolivro2 = campos[1];
                editora2 = campos[2];
                autor2 = campos[3];
                genero2 = campos[4];
                status2 = campos[5];
                voltar2 = campos[6];
            }
            exemplo1.setText(titulo2);
            exemplo2.setText(anodolivro2);
            exemplo3.setText(editora2);
            exemplo4.setText(autor2);
            exemplo5.setText(genero2);
            exemplo6.setText(status2);
            fr.close();
        
        }catch (IOException e){
            System.out.println("Erro na leitura do arquivo.");
        }
    }

